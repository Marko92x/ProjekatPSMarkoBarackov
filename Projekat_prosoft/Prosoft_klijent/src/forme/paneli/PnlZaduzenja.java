    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jtblZaduzenja.getSelectedRowCount() == 1) {
            try {
                Zaduzenje zaduzenje = NitKlijent.getInstance().getZaduzenjaZaDobavljaca().get(jtblZaduzenja.getSelectedRow());
                if (zaduzenje.getRazduzio().getIme() == null) {
                    zaduzenje.setRazduzio(NitKlijent.getInstance().getUlogovani());
                    Date d = new Date(new java.util.Date().getTime());
                    zaduzenje.setDatumRazduzenja(d);
                    KlijentTransferObjekat kto = new KlijentTransferObjekat();
                    kto.setOperacija(Konstante.OPERACIJA_IZMENI_ZADUZENJA);
                    kto.setParametar(zaduzenje);
                    NitKlijent.out.writeObject(kto);
                } else {
                    JOptionPane.showMessageDialog(this, "Vec je razduzeno");
                }
            } catch (IOException ex) {
                Logger.getLogger(PnlZaduzenja.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selektovali sve vise ili nise selektovali ni jedan red");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


