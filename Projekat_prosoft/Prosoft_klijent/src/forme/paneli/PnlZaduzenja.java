    private void jbtnDodajZaduzenjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDodajZaduzenjeActionPerformed
        Zaduzenje z = new Zaduzenje();
        z.setDobavljac(dobavljac);
        Date d = new Date(new java.util.Date().getTime());
        z.setDatumZaduzenja(d);
        z.setZaduzio(NitKlijent.getInstance().getUlogovani());
        z.setDatumRazduzenja(null);

        if (jrbPrevoz.isSelected()) {
            try {
                z.setPrevoz(true);
                KlijentTransferObjekat kto = new KlijentTransferObjekat();
                kto.setOperacija(Konstante.OPERACIJA_UNOS_ZADUZENJA);
                kto.setParametar(z);
                NitKlijent.out.writeObject(kto);
            } catch (IOException ex) {
                Logger.getLogger(PnlZaduzenja.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (jrbKompost.isSelected()) {
            try {
                z.setKompost(true);
                z.setBrojVreca(Integer.parseInt(jtfBrojVreca.getText()));
                KlijentTransferObjekat kto = new KlijentTransferObjekat();
                kto.setOperacija(Konstante.OPERACIJA_UNOS_ZADUZENJA);
                kto.setParametar(z);
                NitKlijent.out.writeObject(kto);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Niste uneli ispravno broj vreca");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Niste selektovali nista!");
        }
    }//GEN-LAST:event_jbtnDodajZaduzenjeActionPerformed

